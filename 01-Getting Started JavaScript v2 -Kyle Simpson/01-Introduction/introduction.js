var teacher = "Kyle";
var twitterHandle = "getify";
var age = 39;

function whoAmI(myName, myNickname, myAge) {
  console.log(`
  Hi, I'm ${myName} (aka ${myNickname}), and I'm ${myAge} years old.`);
}

whoAmI(teacher, twitterHandle, age);

// Hi, I'm Kyle (aka getify), and I'm 39 years old.

